<?php

class Solution {

    /**
     * @param Integer $num
     * @return String[]
     */
    function readBinaryWatch($num)
    {
        if ($num < 0 || $num > 8)
        {
            return [];
        }

        $hours = [
            0 => ['0'],
            1 => ['1', '2', '4', '8'],
            2 => ['3', '5', '9', '6', '10'],
            3 => ['7', '11']
        ];

        $minite = [
            0 => ['00'],
            1 => ['01', '02', '04', '08', '16', '32'],
            2 => ['03', '05', '09', '17', '33', '06', '10', '18', '34', '12', '20', '36', '24', '40', '48'],
            3 => ['07', '11', '19', '35', '13', '21', '37', '25', '41', '49', '14', '22', '38', '42', '50', '28', '44', '52', '56', '26'],
            4 => ['15', '23', '39', '27', '43', '51', '29', '45', '53', '57', '30', '46', '54', '58'],
            5 => ['31', '47', '55', '59'],
        ];

        $res = [];

        for ($i = 0; $i < 4 && $i <= $num; ++$i)
        {
            foreach ($hours[$i] as $hour)
            {
                foreach ($minite[$num - $i] as $min)
                {
                    $res[] = $hour . ':' . $min;
                }
            }
        }

        return $res;
    }
}